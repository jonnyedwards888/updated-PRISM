interface IconLibraryProps {
  isOpen: boolean;
  onClose: () => void;
  onIconSelect: (iconName: string, iconSvg: string) => void;
}

export const IconLibrary: React.FC<IconLibraryProps> = ({ isOpen, onClose, onIconSelect }) => {
  const [searchTerm, setSearchTerm] = useState('');

  const filteredIcons = useMemo(() => {
    if (!searchTerm.trim()) {
      return Object.entries(ICON_LIBRARY);
    }
    
    const term = searchTerm.toLowerCase();
    return Object.entries(ICON_LIBRARY).filter(([name]) => 
      name.toLowerCase().includes(term)
    );
  }, [searchTerm]);

  const handleIconClick = (iconName: string, iconSvg: string) => {
    onIconSelect(iconName, iconSvg);
    onClose();
  };

  if (!isOpen) return null;

  return (
    <div className="icon-library-overlay" onClick={onClose}>
      <div className="icon-library-modal" onClick={(e) => e.stopPropagation()}>
        <div className="icon-library-header">
          <h2>Icon Library</h2>
          <button className="close-button" onClick={onClose}>
            <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
              <path d="M18 6 6 18"/><path d="m6 6 12 12"/>
            </svg>
          </button>
        </div>
        
        <div className="icon-library-search">
          <div className="search-input-wrapper">
            <svg className="search-icon" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
              <circle cx="11" cy="11" r="8"/><path d="m21 21-4.35-4.35"/>
            </svg>
            <input
              type="text"
              placeholder="Search icons..."
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              className="search-input"
              autoFocus
            />
          </div>
        </div>

        <div className="icon-library-grid">
          {filteredIcons.map(([name, svg]) => (
            <div
              key={name}
              className="icon-item"
              onClick={() => handleIconClick(name, svg)}
              title={name}
            >
              <div 
                className="icon-preview"
                dangerouslySetInnerHTML={{ __html: svg }}
              />
              <span className="icon-name">{name}</span>
            </div>
          ))}
        </div>

        {filteredIcons.length === 0 && (
          <div className="no-results">
            <p>No icons found for "{searchTerm}"</p>
            <p>Try a different search term</p>
          </div>
        )}
      </div>
    </div>
  );
};
